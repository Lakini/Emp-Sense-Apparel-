    public PredictUI() throws FileNotFoundException, IOException {
        DBAccessJava dbaccess = DBAccessJava.getDbCon();
        FileReader selectedFile = null;
        
        initComponents();
        selectedFile = new FileReader("src/prediction/apparel/csv/metricsData1.csv");
            CSVReader reader = new CSVReader(selectedFile);
            String[] headers = new String[]{"Model Name","Accuracy","Precision", "Recall", "F1"};
            DefaultTableModel model = new DefaultTableModel(headers,0);
            
            List<String[]> tableEntries = reader.readAll();
            int size = tableEntries.size();
            //Fill table entries and update progressbar
            for(int i = 1; i < size; i++){
                model.addRow(tableEntries.get(i));
            }
           tbmodelDetails.setModel(model);
         
    }

